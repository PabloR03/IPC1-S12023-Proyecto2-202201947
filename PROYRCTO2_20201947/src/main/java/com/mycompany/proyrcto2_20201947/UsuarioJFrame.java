    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(login.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Usuario Invalido"); 
        }else{
            
            NombreUsuario=login.getText();
            Usuario usuarioExistente = (Usuario)AppState.listaUsuario.find(NombreUsuario);
            
            if(usuarioExistente!=null){
                AppState.listaUsuario.ImprimirUsuarios();
                Biblioteca biblioteca = new Biblioteca();
                biblioteca.NombreBiblioteca.setText(NombreUsuario);
                biblioteca.setVisible(true);
                dispose();
                
                JOptionPane.showMessageDialog(null, "Bienvenido");
            }else{
                
                JOptionPane.showMessageDialog(null, "Nuevo Usuario");
                
                Usuario nuevoUsuario = new Usuario();
                Categoria nuevaCategoria = new Categoria();
                nuevaCategoria.setNombreCategoria("General");
                nuevoUsuario.setNombreUsuario(NombreUsuario);
                nuevoUsuario.listaCategoria.add(nuevaCategoria);
                AppState.listaUsuario.add(nuevoUsuario);
                
                Biblioteca biblioteca = new Biblioteca();
                biblioteca.NombreBiblioteca.setText(NombreUsuario);
                biblioteca.setVisible(true);
                dispose();
                
                AppState.listaUsuario.ImprimirUsuarios();
                JOptionPane.showMessageDialog(null, "Bienvenido");
                
            }
            
        }
        
        
    }//GEN-LAST:event_jButton2ActionPerformed


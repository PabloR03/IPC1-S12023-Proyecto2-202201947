    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        Usuario usuarioActual = (Usuario) AppState.listaUsuario.find(NombreUsuario);
        String nombreCategoria=jTextField1.getText();
        if(jTextField1.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Categoria invalida");
        }else{
            boolean existe=false;
            for (Categoria categoria : usuarioActual.getListaCategoria()) {
                if(categoria.getNombreCategoria().equals(jTextField1.getText())) {
                    existe = true;
                    break;
                }
            }
            if(existe){
                JOptionPane.showMessageDialog(null, "Categoria Repetida");
            }else{
                Categoria nuevaCategoria = new Categoria();
                nuevaCategoria.setNombreCategoria(nombreCategoria);
                usuarioActual.agregarCategoria(nuevaCategoria);
                DefaultListModel<String> modelo2 = (DefaultListModel<String>)jList1.getModel();
                modelo2.addElement(nuevaCategoria.getNombreCategoria());
                jList1.setModel(modelo2);
                JOptionPane.showMessageDialog(null, "Agregada Correctamente");
                
                usuarioActual.ImprimirCategorias();
                jTextField1.setText(null);
                jTextField2.setText(null);
                jLabel4.setIcon(null);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        Usuario usuarioActual = (Usuario) AppState.listaUsuario.find(NombreUsuario);
        if(jTextField1.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Campo invalido");
        }else if(jTextField1.getText().equalsIgnoreCase("General")){
             JOptionPane.showMessageDialog(null, "No es posible eliminar la categoria General");
        }else{
            boolean existe=false;
            for (Categoria categoria : usuarioActual.getListaCategoria()) {
                if (categoria.getNombreCategoria().equals(jTextField1.getText())) {
                    existe=true;
                    JOptionPane.showMessageDialog(null, "Eliminada con exito");
                    usuarioActual.getListaCategoria().remove(categoria);
                    
                    
                    usuarioActual.ImprimirCategorias();
                    jTextField1.setText(null);
                    jTextField2.setText(null);
                    jLabel4.setIcon(null);
                    break;
                }
            }
            if(!existe){
                JOptionPane.showMessageDialog(null, "Nombre erroneo");
            }else{
                DefaultListModel<String> modelo3 = new DefaultListModel<>();
                for (Categoria categoria : usuarioActual.getListaCategoria()) {
                    modelo3.addElement(categoria.getNombreCategoria());
                }
                jList1.setModel(modelo3);
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed


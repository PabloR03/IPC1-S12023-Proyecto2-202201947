    private void jList1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList1MouseClicked
        // TODO add your handling code here:
        Usuario usuarioActual = (Usuario) AppState.listaUsuario.find(NombreUsuario); 
        Categoria categoria = (Categoria)usuarioActual.listaCategoria.get(jList1.getSelectedIndex());
        String NombreImagen;
        if(categoria!=null) {
            ListaDoble listaImagenes = categoria.getListaImagen();
            if (listaImagenes.get(0)!=null) {
                Imagen iconImagen = (Imagen) listaImagenes.get(0);
                NombreImagen =iconImagen.nombre;
                iconoImagen= iconImagen.getImagenIcon();
                Image Imagen = iconoImagen.getImage();
                Imagen = Imagen.getScaledInstance(jLabel4.getWidth(), jLabel4.getHeight(), Image.SCALE_DEFAULT);
                jLabel4.setIcon(new ImageIcon(Imagen));
                NOMfoto.setText(NombreImagen);
            }else{
                NOMfoto.setText(null);
                jLabel4.setIcon(null); 
            }
        } 
    }//GEN-LAST:event_jList1MouseClicked

